// Асоціація для кани; дакутен/хандакутен-варіанти повертають образ базової
// кани з приміткою. '' — коли асоціації немає (некана, малі кани, ー).
export const mnemonicFor = (kana: string): string => {
  const direct = MNEMONICS[kana]
  if (direct) return direct

  const decomposed = kana.normalize('NFD')
  if (decomposed.length < 2) return ''
  const base = MNEMONICS[decomposed[0] ?? '']
  if (!base) return ''
  const mark = decomposed.codePointAt(1)
  if (mark === 0x3099) return `${base} — плюс ゛: той самий образ, звук дзвінкий`
  if (mark === 0x309a) return `${base} — плюс ゜: той самий образ, звук «п»`
  return ''
}
