// Кластери візуально схожих кан («мінімальні пари») для режиму «Кат»:
// саме їх найчастіше плутають, тож дрил подає їх упереміш, а формат «вибір»
// бере дистрактори з того самого кластера.

export const MINIMAL_PAIR_CLUSTERS: string[][] = [
  // Катакана — класика жанру.
  ['シ', 'ツ'],
  ['ソ', 'ン'],
  ['ク', 'タ', 'ケ'],
  ['ウ', 'フ', 'ワ'],
  ['コ', 'ユ', 'ヨ'],
  ['チ', 'テ'],
  ['ナ', 'メ'],
  ['ル', 'レ'],
  ['ニ', 'ミ'],
  ['ア', 'マ', 'ム'],
  ['ヌ', 'ス'],
  ['ホ', 'オ'],
  // Хіраґана.
  ['は', 'ほ', 'ま'],
  ['ね', 'れ', 'わ'],
  ['ぬ', 'め'],
  ['る', 'ろ'],
  ['さ', 'き', 'ち'],
  ['た', 'な'],
  ['う', 'つ'],
  ['い', 'り'],
  ['く', 'へ'],
  ['し', 'も'],
  ['お', 'あ'],
  ['ば', 'ぱ', 'ぼ', 'ぽ'],
]

const CLUSTER_BY_KANA = new Map<string, string[]>()
for (const cluster of MINIMAL_PAIR_CLUSTERS) {
  for (const kana of cluster) CLUSTER_BY_KANA.set(kana, cluster)
}

// Сусіди кани по кластеру (без неї самої); порожній масив, якщо кана не
// входить у жоден кластер.
export const clusterFor = (kana: string): string[] =>
  (CLUSTER_BY_KANA.get(kana) ?? []).filter((k) => k !== kana)

// Усі кани з усіх кластерів — холодний старт «Ката», коли статистика порожня.
export const ALL_CLUSTER_KANA: string[] = [...CLUSTER_BY_KANA.keys()]
